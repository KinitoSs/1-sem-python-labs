# Вводим список времен с клавиатуры
time_list = input("Введите список времен через пробел: ").split()

# Создаем пустой словарь для подсчета встречаемости времен
time_count = {}

# Используем цикл for для перебора каждого времени в списке
for time in time_list:
    # Если время уже есть в словаре, увеличиваем его счетчик на 1
    if time in time_count:
        time_count[time] += 1
    # Если время встречается впервые, добавляем его в словарь с начальным счетчиком 1
    else:
        time_count[time] = 1

# Выводим результат на экран
print("Список различных времен и их количество:")
for time, count in time_count.items():
    print(time, "встречается", count, "раз(а)")