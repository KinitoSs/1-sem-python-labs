# Задаем строку для анализа
text = "Привет, мир! Как дела?"

# Инициализируем переменные для хранения количества букв и знаков препинания
letters_count = 0
punctuation_count = 0

# Проходим циклом по всем символам строки
for char in text:
    # Если символ является буквой, увеличиваем счетчик букв
    if char.isalpha():
        letters_count += 1
    # Если символ является знаком препинания, увеличиваем счетчик знаков препинания
    elif char in ",.!?":
        punctuation_count += 1

# Выводим на экран количество букв и знаков препинания в строке
print("Количество букв в строке:", letters_count)
print("Количество знаков препинания в строке:", punctuation_count)
