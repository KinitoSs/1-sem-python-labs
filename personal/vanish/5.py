# Создаем пустой список для хранения электронных адресов
emails = []

# Вводим количество электронных адресов
n = int(input("Введите количество электронных адресов: "))

# Вводим каждый электронный адрес и добавляем его в список
for i in range(n):
    email = input("Введите электронный адрес №" + str(i + 1) + ": ")
    emails.append(email)

# Создаем пустой словарь для хранения частоты встречаемости каждого электронного адреса
freq = {}

# Проходим по каждому электронному адресу в списке
for email in emails:
    # Если электронный адрес уже есть в словаре, увеличиваем его значение на единицу
    if email in freq:
        freq[email] += 1
    # Если электронного адреса нет в словаре, добавляем его с значением единицы
    else:
        freq[email] = 1

# Выводим результаты на экран
print("Частота встречаемости различных электронных адресов:")
for email, count in freq.items():
    print(email, "-", count)
