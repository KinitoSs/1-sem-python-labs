# –°–ª–æ–∂–µ–Ω–∏–µ –ø—Ä–æ–¥–æ–ª–∂–∞–π—Ç–µ –¥–æ —Ç–µ—Ö –ø–æ—Ä, –ø–æ–∫–∞ —Å—É–º–º–∞ —á–∏—Å–µ–ª –Ω–µ –ø—Ä–µ–≤—ã—Å–∏—Ç —á–∏—Å–ª–∞ n, –≤–≤–æ–¥–∏–º–æ–≥–æ
# —Å –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã.

m = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ m: "))
n = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ n: "))

sum_of_squares = 0

i = 1
while sum_of_squares <= n:
    sum_of_squares += i**2
    i += 1

sum_of_squares -= i**2

print("–°—É–º–º–∞ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ", m, "—Ä–∞–≤–Ω–∞", sum(i**2 for i in range(1, m + 1)))
print("–°—É–º–º–∞ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ", i - 1, "—Ä–∞–≤–Ω–∞", sum_of_squares)
